package VariaveisOperadoresMatematicosEntradaeSaida;

public class ConversaoDeCelsiusParaFarenheit {
    public static void main(String[] args){
        double celsius = 32.5;
        double farenheit = 90.5;
        farenheit= (celsius * 1.8) + 32;

        System.out.println(" O resultado desta equação é: " + farenheit);
    }
}
